package rep1;

public class AssignmentOperator {

	public static void main(String[] args) {
		
		int x = 50;
		int y = 30;
		int z = 5;
		int x1 = 8;
		int y1 = 9;
		
		x += 50;//x = x + 50;
		y -= 20;//y = y - 20;
		z *= 10;//z = z * 10;
		x1 /= 3;//x1 = x1 / 3;
		y1 %= 2;//y1 = y1 % 2;
		
		System.out.println(x);
		System.out.println(y);
		System.out.println(z);
		System.out.println(x1);
		System.out.println(y1);

	}

}
