package rep1;

public class Variable {

	public static void main(String[] args) {
		int value1 = 10;//integer variable data type
		int value2 = 20;
		int result = 	value1 + value2;		
		System.out.println("Output result is " + result);
		
		boolean flag = 4 > 5;//boolean variable data type
		System.out.println("Boolean result is " + flag);
		
		String name = "Su Cherry";//String data type
		System.out.println("Your name is " + name);
		
	}

}
